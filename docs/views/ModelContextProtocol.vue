<script>
import { styleTokens } from '@/helpers/props.js';

import DoxenCodeSwapper from '@/components/DoxenCodeSwapper.vue';
import DocumentationSection from '@@@/components/DocumentationSection.vue';

import {
  MCP_ADVANCED_OPTIONS_EXAMPLE,
  MCP_ADVANCED_COMPOSITION_EXAMPLE,
  MCP_ADVANCED_SCRIPT_SETUP_EXAMPLE
} from '@@@/helpers/codeSnippets.js';

const CUSTOM_SCRIPT = `
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';

import { serializeDemos } from 'vue-doxen';

import { demo as MyFoo } from '../src/demos/fooDemo.js';
import { demo as MyBar } from '../src/demos/barDemo.js';
import MyBuzz from '../src/components/MyBuzz.vue';

const demos = {
  MyFoo,
  MyBar,
  MyBuzz
};

async function convertDemosToJson () {
  try {
    const data = await serializeDemos(demos);
    const content = JSON.stringify(data, null, 2) + '\\n';
    const outputFile = join(import.meta.dirname, 'demos.json');
    fs.writeFileSync(outputFile, content);
  } catch (error) {
    console.log(error);
  }
}

convertDemosToJson();
`.trim();

const PACKAGE_JSON = `
{
  "scripts": {
    "mcp": "vite-node --config vite.config.js ./scripts/generateMcpData.js"
  }
}
`.trim();
const ADVANCED = `
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';

import { serializeDemos } from 'vue-doxen';

import { allDemos } from '../src/demos/index.js';

async function convertDemosToJson () {
  try {
    // A URL to a webpage with all the demos loaded
    const url = 'https://localhost:8080/doxen-mcp';

    const data = await serializeDemos(allDemos, url);
    const content = JSON.stringify(data, null, 2) + '\\n';

    const outputFile = join(import.meta.dirname, 'demos.json');
    fs.writeFileSync(outputFile, content);
  } catch (error) {
    console.log(error);
  }
}

convertDemosToJson();
`.trim();

const ALL_DEMOS = `
import { demo as MyFoo } from './fooDemo.js';
import { demo as MyBar } from './barDemo.js';
import MyBuzz from '../components/MyBuzz.vue';

export const allDemos = {
  MyFoo,
  MyBar,
  MyBuzz
};
`.trim();

export default {
