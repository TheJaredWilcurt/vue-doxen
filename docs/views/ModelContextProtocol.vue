    <DocumentationSection id="advanced" title="Advanced">
      <p>
        Vue-Doxen demos allow using custom components for your
        <code>description</code>, <code>importStatement</code>, etc.
        Custom Vue components can be extremely complex. In order to
        extract the information from them to serialize it as a string
        of HTML, we use Playwright to load your demo in a real browser
        and extract the rendered HTML from your custom components. This
        is then placed in the JSON object generated by
        <code>serializeDemos</code>.
      </p>
